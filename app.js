app.post('/order/checkout', async (req, res) => {
  if (req.session.userType !== 'customer') return res.redirect('/login');

  const paymentMode = (req.body.payment_mode || '').trim();
  const deliveryAddress = (req.body.delivery_address || '').trim();

  if (!paymentMode || !deliveryAddress) {
    return res.redirect('/order?error=' + encodeURIComponent('Payment mode and delivery address are required'));
  }

  let conn;

  try {
    const cart = sanitizeCart(req);
    if (!cart.length) {
      return res.redirect('/order?error=' + encodeURIComponent('Your cart is empty'));
    }

    conn = await pool.getConnection();
    const cartState = await buildCartState(req, conn);

    if (!cartState.items.length) {
      return res.redirect('/order?error=' + encodeURIComponent('Your cart is empty'));
    }

    const invalidItem = cartState.items.find((item) => item.is_expired || item.has_stock_issue);
    if (invalidItem) {
      const message = invalidItem.is_expired
        ? `${invalidItem.product_name} is expired and cannot be ordered`
        : `${invalidItem.product_name} does not have enough stock`;
      return res.redirect('/order?error=' + encodeURIComponent(message));
    }

    const createdOrders = [];

    for (const item of cartState.items) {
      const orderResult = await conn.execute(
        `
          BEGIN
            place_grouped_order_transaction(
              :customer_id,
              :product_id,
              :quantity,
              :price,
              :original_price,
              :discount_percent,
              :payment_mode,
              :delivery_address,
              :order_id
            );
          END;
        `,
        {
          customer_id: req.session.user.customer_id,
          product_id: item.product_id,
          quantity: item.quantity,
          price: item.discounted_price_per_kg,
          original_price: item.original_price_per_kg,
          discount_percent: item.discount_percent,
          payment_mode: paymentMode,
          delivery_address: deliveryAddress,
          order_id: { dir: oracledb.BIND_OUT, type: oracledb.NUMBER }
        }
      );

      createdOrders.push(orderResult.outBinds.order_id);
    }

    await conn.commit();
    req.session.cart = [];
      res.redirect(`/order?message=${encodeURIComponent(`Placed ${createdOrders.length} order(s) successfully`)}`);
  } catch (err) {
    if (conn) await conn.rollback();
    res.redirect('/order?error=' + encodeURIComponent(err.message));
  } finally {
    if (conn) await conn.close();
  }
});

