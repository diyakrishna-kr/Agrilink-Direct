app.post('/admin/farmer/:farmerId/delete', async (req, res) => {
  if (req.session.userType !== 'admin') return res.redirect('/login');

  const farmerId = Number(req.params.farmerId);
  let conn;

  try {
    conn = await pool.getConnection();

    const farmerResult = await conn.execute(
      `SELECT name
       FROM farmer
       WHERE farmer_id = :1`,
      [farmerId]
    );

    if (farmerResult.rows.length === 0) {
      return res.redirect('/admin?error=' + encodeURIComponent('Farmer not found'));
    }

    const productResult = await conn.execute(
      `SELECT product_id
       FROM product
       WHERE farmer_id = :1`,
      [farmerId]
    );
    const productIds = productResult.rows.map((row) => Number(Array.isArray(row) ? row[0] : row.PRODUCT_ID));

    if (productIds.length) {
      const orderResult = await conn.execute(
        `SELECT DISTINCT order_id
         FROM order_details
         WHERE product_id IN (${buildInClause(productIds)})`,
        productIds
      );
      const orderIds = orderResult.rows.map((row) => Number(Array.isArray(row) ? row[0] : row.ORDER_ID));

      if (orderIds.length) {
        await conn.execute(
          `DELETE FROM product_reviews
           WHERE order_id IN (${buildInClause(orderIds)})`,
          orderIds
        );

        await conn.execute(
          `DELETE FROM payment
           WHERE order_id IN (${buildInClause(orderIds)})`,
          orderIds
        );
      }

      await conn.execute(
        `DELETE FROM order_details
         WHERE product_id IN (${buildInClause(productIds)})`,
        productIds
      );

      if (orderIds.length) {
        await conn.execute(
          `DELETE FROM orders
           WHERE order_id IN (${buildInClause(orderIds)})`,
          orderIds
        );
      }
    }

    await conn.execute(
      `DELETE FROM product
       WHERE farmer_id = :1`,
      [farmerId]
    );

    await conn.execute(
      `DELETE FROM farmer
       WHERE farmer_id = :1`,
      [farmerId]
    );

    await conn.commit();
    res.redirect('/admin?message=' + encodeURIComponent('Farmer deleted successfully'));
  } catch (err) {
    if (conn) await conn.rollback();
    res.redirect('/admin?error=' + encodeURIComponent(err.message));
  } finally {
    if (conn) await conn.close();
  }
});

