// ---------------- HELPERS ----------------
async function runQuery(sql, binds = [], options = {}) {
  let conn;
  try {
    conn = await pool.getConnection();
    return await conn.execute(sql, binds, options);
  } finally {
    if (conn) await conn.close();
  }
}

function toLowerKeys(row) {
  const obj = {};
  for (const key in row) obj[key.toLowerCase()] = row[key];
  return obj;
}

function parseNumber(value) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : NaN;
}

function roundCurrency(value) {
  return Math.round(value * 100) / 100;
}

function getSessionCart(req) {
  if (!Array.isArray(req.session.cart)) {
    req.session.cart = [];
  }
  return req.session.cart;
}

function startOfDay(dateValue) {
  const date = new Date(dateValue);
  date.setHours(0, 0, 0, 0);
  return date;
}

function formatDateInput(dateValue) {
  if (!dateValue) return '';
  const date = new Date(dateValue);
  if (Number.isNaN(date.getTime())) return '';
  return date.toISOString().slice(0, 10);
}

function enrichProduct(product) {
  const originalPrice = Number(product.price_per_kg);
  const manualDiscountPercent = Number(product.manual_discount_percent || 0);
  const reviewCount = Number(product.review_count || 0);
  const averageRating = reviewCount ? Number(product.average_rating || 0) : 0;
  const expiryDate = product.expiry_date ? new Date(product.expiry_date) : null;
  const daysUntilExpiry = product.days_until_expiry !== undefined && product.days_until_expiry !== null
    ? Number(product.days_until_expiry)
    : (expiryDate && !Number.isNaN(expiryDate.getTime())
        ? Math.ceil((startOfDay(expiryDate) - startOfDay(new Date())) / (1000 * 60 * 60 * 24))
        : null);
  const isExpired = product.is_expired !== undefined
    ? Boolean(Number(product.is_expired))
    : daysUntilExpiry !== null && daysUntilExpiry < 0;
  const isNearExpiry = product.is_near_expiry !== undefined
    ? Boolean(Number(product.is_near_expiry))
    : daysUntilExpiry !== null && daysUntilExpiry >= 0 && daysUntilExpiry <= NEAR_EXPIRY_DAYS;
  const discountPercent = product.discount_percent !== undefined && product.discount_percent !== null
    ? Number(product.discount_percent)
    : Math.max(manualDiscountPercent, isNearExpiry ? NEAR_EXPIRY_DISCOUNT_PERCENT : 0);
  const discountedPrice = product.discounted_price_per_kg !== undefined && product.discounted_price_per_kg !== null
    ? Number(product.discounted_price_per_kg)
    : roundCurrency(originalPrice * (1 - discountPercent / 100));
  const nearExpiryDiscount = isNearExpiry ? NEAR_EXPIRY_DISCOUNT_PERCENT : 0;

  return {
    ...product,
    price_per_kg: originalPrice,
    original_price_per_kg: originalPrice,
    discounted_price_per_kg: discountedPrice,
    discount_percent: discountPercent,
    manual_discount_percent: manualDiscountPercent,
    review_count: reviewCount,
    average_rating: averageRating,
    near_expiry_discount_percent: nearExpiryDiscount,
    days_until_expiry: daysUntilExpiry,
    is_near_expiry: isNearExpiry,
    is_expired: isExpired,
    expiry_input_value: formatDateInput(expiryDate),
    expiry_display: expiryDate && !Number.isNaN(expiryDate.getTime())
      ? expiryDate.toLocaleDateString()
      : 'Not set'
  };
}

function buildInClause(values, offset = 1) {
  return values.map((_, index) => `:${index + offset}`).join(', ');
}

async function fetchProductsByIds(productIds, conn = null) {
  if (!productIds.length) {
    return [];
  }

  const sql = `
    SELECT *
    FROM product_catalog_view
    WHERE product_id IN (${buildInClause(productIds)})
  `;

  const result = conn
    ? await conn.execute(sql, productIds)
    : await runQuery(sql, productIds);

  return result.rows.map((row) => enrichProduct(toLowerKeys(row)));
}

async function buildCartState(req, conn = null) {
  const cart = getSessionCart(req);
  if (!cart.length) {
    return {
      items: [],
      groups: [],
      total: 0,
      totalOriginal: 0,
      savings: 0,
      hasIssues: false
    };
  }

  const productIds = [...new Set(cart.map((item) => Number(item.product_id)).filter(Boolean))];
  const products = await fetchProductsByIds(productIds, conn);
  const productMap = new Map(products.map((product) => [Number(product.product_id), product]));

  const items = [];
  for (const cartItem of cart) {
    const product = productMap.get(Number(cartItem.product_id));
    if (!product) {
      continue;
    }

    const quantity = parseNumber(cartItem.quantity);
    if (!quantity || quantity <= 0) {
      continue;
    }

    const subtotal = roundCurrency(quantity * product.discounted_price_per_kg);
    const originalSubtotal = roundCurrency(quantity * product.original_price_per_kg);

    items.push({
      ...product,
      quantity,
      subtotal,
      original_subtotal: originalSubtotal,
      savings: roundCurrency(originalSubtotal - subtotal),
      has_stock_issue: quantity > Number(product.available_quantity)
    });
  }

  const groupsMap = new Map();
  for (const item of items) {
    if (!groupsMap.has(item.farmer_id)) {
      groupsMap.set(item.farmer_id, {
        farmer_id: item.farmer_id,
        farmer_name: item.farmer_name,
        items: [],
        total: 0,
        totalOriginal: 0,
        savings: 0
      });
    }

    const group = groupsMap.get(item.farmer_id);
    group.items.push(item);
    group.total = roundCurrency(group.total + item.subtotal);
    group.totalOriginal = roundCurrency(group.totalOriginal + item.original_subtotal);
    group.savings = roundCurrency(group.savings + item.savings);
  }

  const groups = Array.from(groupsMap.values());
  return {
    items,
    groups,
    total: roundCurrency(items.reduce((sum, item) => sum + item.subtotal, 0)),
    totalOriginal: roundCurrency(items.reduce((sum, item) => sum + item.original_subtotal, 0)),
    savings: roundCurrency(items.reduce((sum, item) => sum + item.savings, 0)),
    hasIssues: items.some((item) => item.is_expired || item.has_stock_issue)
  };
}

function sanitizeCart(req) {
  const cart = getSessionCart(req)
    .map((item) => ({
      product_id: Number(item.product_id),
      quantity: parseNumber(item.quantity)
    }))
    .filter((item) => item.product_id && item.quantity && item.quantity > 0);

  req.session.cart = cart;
  return cart;
}

async function loadProducts(searchTerm = '', categoryFilter = '', groupFilter = '') {
  let query = `
    SELECT *
    FROM product_catalog_view p
  `;
  const params = [];
  const whereClauses = [];

  if (searchTerm) {
    whereClauses.push(`(
      LOWER(p.product_name) LIKE LOWER(:${params.length + 1})
      OR LOWER(p.category) LIKE LOWER(:${params.length + 2})
      OR LOWER(p.farmer_name) LIKE LOWER(:${params.length + 3})
    )`);
    params.push(`%${searchTerm}%`, `%${searchTerm}%`, `%${searchTerm}%`);
  }

  if (categoryFilter) {
    whereClauses.push(`LOWER(p.category) = LOWER(:${params.length + 1})`);
    params.push(categoryFilter);
  }

  if (groupFilter === 'cereals') {
    whereClauses.push(`(
      LOWER(p.product_name) LIKE LOWER(:${params.length + 1})
      OR LOWER(p.product_name) LIKE LOWER(:${params.length + 2})
      OR LOWER(p.product_name) LIKE LOWER(:${params.length + 3})
      OR LOWER(p.product_name) LIKE LOWER(:${params.length + 4})
    )`);
    params.push('%rice%', '%wheat%', '%bajra%', '%jowar%');
  }

  if (groupFilter === 'pulses') {
    whereClauses.push(`(
      LOWER(p.product_name) LIKE LOWER(:${params.length + 1})
      OR LOWER(p.product_name) LIKE LOWER(:${params.length + 2})
      OR LOWER(p.product_name) LIKE LOWER(:${params.length + 3})
      OR LOWER(p.product_name) LIKE LOWER(:${params.length + 4})
    )`);
    params.push('%dal%', '%moong%', '%urad%', '%gram%');
  }

  if (whereClauses.length) {
    query += ` WHERE ${whereClauses.join(' AND ')}`;
  }

  query += ` ORDER BY p.product_id DESC`;
  const result = await runQuery(query, params);
  return result.rows.map((row) => enrichProduct(toLowerKeys(row)));
}

