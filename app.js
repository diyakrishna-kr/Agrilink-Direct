app.post('/cart/add', async (req, res) => {
  if (req.session.userType !== 'customer') return res.redirect('/login');

  const productId = Number(req.body.product_id);
  const quantity = parseNumber(req.body.quantity);
  const redirectTo = req.body.redirect_to || '/order';

  if (!productId || !quantity || quantity <= 0) {
    return res.redirect(`${redirectTo}?error=${encodeURIComponent('Invalid cart quantity')}`);
  }

  try {
    const products = await fetchProductsByIds([productId]);
    const product = products[0];

    if (!product) {
      return res.redirect(`${redirectTo}?error=${encodeURIComponent('Product not found')}`);
    }
    if (product.is_expired) {
      return res.redirect(`${redirectTo}?error=${encodeURIComponent('Expired products cannot be added to cart')}`);
    }
    if (quantity > Number(product.available_quantity)) {
      return res.redirect(`${redirectTo}?error=${encodeURIComponent('Requested quantity exceeds stock')}`);
    }

    const cart = sanitizeCart(req);
    const existing = cart.find((item) => Number(item.product_id) === productId);
    if (existing) {
      existing.quantity = roundCurrency(existing.quantity + quantity);
    } else {
      cart.push({ product_id: productId, quantity });
    }

    req.session.cart = cart;
    res.redirect(`${redirectTo}?message=${encodeURIComponent('Added to cart')}`);
  } catch (err) {
    res.redirect(`${redirectTo}?error=${encodeURIComponent(err.message)}`);
  }
});

